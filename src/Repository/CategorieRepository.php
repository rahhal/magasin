    public function findOneByDesignation($value): ?Categorie
    {
        return $this->createQueryBuilder('c')
            ->andWhere('c.designation = :val')
            ->setParameter('val', $value)
            ->getQuery()
            ->getOneOrNullResult()
        ;
    }
    public function findCategorieByUser($id)
    {
        return $this->createQueryBuilder('c')
            ->innerJoin('c.user', 'u')
            ->andWhere('u.id = :id')
            ->setParameter('id', $id)
            ->getQuery()
            // ->getOneOrNullResult();
            ->getResult();
    }
    public function findByCurrentUser($user)
    {
        return $this
            ->createQueryBuilder('c')
            ->andWhere('c.user = :user')
            ->setParameter('user', $user)
            ;
    }
